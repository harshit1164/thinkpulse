# Function to explain the result of a bias test
# This function takes the result of a bias test and prints a user-friendly explanation


def explain_bias_result(result, language="en"):
    p = result["p_value"]
    by = result["by"]
    target = result["target"]
    significant = result["significant"]

    if language == "hi":
        print(f"'{by}' рдФрд░ '{target}' рдХреЗ рдмреАрдЪ рдкреВрд░реНрд╡рд╛рдЧреНрд░рд╣ рдкрд░реАрдХреНрд╖рдг:")
        print(f"p-рдореВрд▓реНрдп = {p}")
        if significant:
            print("рд╕рд╛рдВрдЦреНрдпрд┐рдХреАрдп рд░реВрдк рд╕реЗ рдорд╣рддреНрд╡рдкреВрд░реНрдг рдкреВрд░реНрд╡рд╛рдЧреНрд░рд╣ рдкрд╛рдпрд╛ рдЧрдпрд╛ рд╣реИред")
        else:
            print("рдХреЛрдИ рдкреВрд░реНрд╡рд╛рдЧреНрд░рд╣ рдирд╣реАрдВ рдкрд╛рдпрд╛ рдЧрдпрд╛ред")
    else:
        print(f"Bias test between '{by}' and '{target}':")
        print(f"p-value = {p}")
        if significant:
            print("Statistically significant bias detected.")
        else:
            print("No significant bias detected.")
